package ac.ucl.language.model;

public class Parameter {
    private String type;
    private String id;

    public Parameter(String type, String id) {
        this.type = type;
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }


    @Override
    public String toString() {
        return type + " " + id;
    }
}
